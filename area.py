b = float(input())
c = float(input())
triangle = (a * c) / 2
circle = 3.14159 * c * c
trapezium = ((a + b) * c) / 2
square = b * b
rectangle = a * b
print('TRIANGULO: {:.3f}\nCIRCULO: {:.3f}\nTRAPEZIO: {:.3f}\nQUADRADO: {:.3f}\nRETANGULO: {:.3f}'.format(triangle, circle, trapezium, square, rectangle))
